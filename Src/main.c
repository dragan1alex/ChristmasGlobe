void powerUp()
{
	statusLed(1);
	HAL_Delay(300);
	for(uint8_t i=1 ; i<=3 ; i++)
	{
		for(uint8_t j=0 ; j<=100 ; j++)
		{
			setLed(i, j*2);
			setLed(7-i, j*2);
			HAL_Delay(1);
		}
	}

	for(uint8_t i=100 ; i>=0 && i<200 ; i--) // for uint8_t, 0 - 1 = 255
	{
		setLed(1, i*2);
		setLed(3, i*2);
		setLed(4, i*2);
		setLed(6, i*2);
		HAL_Delay(3);
	}
	for(uint8_t i=100 ; i>=0 && i<200 ; i--)
	{
		setLed(2, i*2);
		setLed(5, i*2);
		HAL_Delay(3);
	}
	HAL_Delay(500);
	statusLed(0);
}

